package com.huayu.irla.manage.service.courseware.jms.adapter;

import java.io.Serializable;

public class Product implements Serializable {
	 /**
	 * 
	 */
	private static final long serialVersionUID = 2872282590895326401L;

	
	/**
	 * 上传的资源id
	 */
	private Long fileId;
	
	/**
	 * 用户名
	 * @return
	 */
	private String userCode;
	
	
	/**
	 * 平台标识
	 * @return
	 */
	private String plateformCode;


	public Long getFileId() {
		return fileId;
	}


	public void setFileId(Long fileId) {
		this.fileId = fileId;
	}


	public String getUserCode() {
		return userCode;
	}


	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}


	public String getPlateformCode() {
		return plateformCode;
	}


	public void setPlateformCode(String plateformCode) {
		this.plateformCode = plateformCode;
	}

	
}
